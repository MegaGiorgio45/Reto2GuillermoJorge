package reto;

public class Funciones1 {

	public static double celsius(double x) {
		return (x * (9 / 5)) + 32;
	}

	public static double fahrenheit(double x) {
		return (x - 32) * 5 / 9;
	}
	
	public static double kilometro(double x) {
		return x/1.609;
	}
	public static double milla(double x) {
		return x*1.609;
	}
}
