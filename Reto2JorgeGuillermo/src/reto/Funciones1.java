package reto;

public class Funciones1 {

	public static double kilo(double x) {
		return x*2.20462;
	}

	public static double libra(double x) {
		return x/2.20462;
	}
	
	public static double kilometro(double x) {
		return x/1.609;
	}
	public static double milla(double x) {
		return x*1.609;
	}
}
